/*Programa que calcula algunas expresiones logicas*/

#include<stdio.h>
int main(){
	
	printf("expresion logica 1: %d\n", (5 > 3 && 10 <= 10));
	printf("expresion logica 2: %d\n", (50 > 75 || 100 < 57));
	printf("expresion logica 3: %d\n", (5 >= 3));
	printf("expresion logica 4: %d\n", !(5 >= 3));
	printf("expresion logica 5: %d\n", (5 >= 3));
}


